export const getSentencesByLevel = (level) => {
  return sentencePractice[level]?.sentences || []
}

export const getAllSentences = () => {
  return Object.values(sentencePractice).flatMap(level => level.sentences)
}

export const getSentencesByDifficulty = (minDifficulty, maxDifficulty) => {
  return getAllSentences().filter(sentence => 
    sentence.difficulty >= minDifficulty && sentence.difficulty <= maxDifficulty
  )
}

export const generateRandomOptions = (correctWord, words, count = 3) => {
  const options = [correctWord]
  const otherWords = words.filter(w => w !== correctWord)
  
  while (options.length < count && otherWords.length > 0) {
    const randomIndex = Math.floor(Math.random() * otherWords.length)
    const word = otherWords.splice(randomIndex, 1)[0]
    if (!options.includes(word)) {
      options.push(word)
    }
  }
  
  // 如果还不够选项，添加一些随机词汇
  const vocabularyWords = ['quick', 'brown', 'fox', 'jumps', 'over', 'lazy', 'dog', 'big', 'small', 'red', 'blue', 'green']
  while (options.length < count && vocabularyWords.length > 0) {
    const randomIndex = Math.floor(Math.random() * vocabularyWords.length)
    const word = vocabularyWords.splice(randomIndex, 1)[0]
    if (!options.includes(word)) {
      options.push(word)
    }
  }
  
  return options.sort(() => Math.random() - 0.5)
}